# Create a Python virtual environment named 'venv'
python3 -m venv venv

# Activate the virtual environment
source venv/bin/activate

# Install the pycryptodome library for cryptography
pip install pycryptodome

# Install the BeautifulSoup4 library for HTML and XML parsing
pip install beautifulsoup4

# Install the requests library for HTTP requests
pip install requests

# Install the autoslot library for automatic slots in Python classes
pip install autoslot

# Install the pronotepy library to interact with the Pronote API
pip install pronotepy